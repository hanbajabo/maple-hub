/**
 * 펜살리르 아이템의 적절한 메시지 배열 반환
 */
export function getPensalirMessage(slot: string, itemName: string): string[] {
    // 무기 판별
    const isWeapon = slot === '무기' || itemName.includes('우트가르드');

    // 모자/한벌옷 판별
    const isHatOverall = itemName.includes('모자') || itemName.includes('한벌옷');

    if (isWeapon) {
        return PENSALIR_MESSAGES.WEAPON;
    } else if (isHatOverall) {
        return PENSALIR_MESSAGES.HAT_OVERALL;
    } else {
        return PENSALIR_MESSAGES.ARMOR;
    }
}
