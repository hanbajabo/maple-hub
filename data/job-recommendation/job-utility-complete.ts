export function getJobUtilities(job: string): JobUtility[] { return JOB_UTILITY_COMPLETE.filter(u => u.job === job) }
export function hasUtilityCategory(job: string, cat: UtilityCategory): boolean { return JOB_UTILITY_COMPLETE.some(u => u.job === job && u.category === cat) }
export function getUtilityFlags(job: string) {
    return {
        hasDebuff: hasUtilityCategory(job, '능력치 감소'),
        hasBuffIgnore: hasUtilityCategory(job, '버프 무시'),
        hasBind: hasUtilityCategory(job, '행동 불능'),
        hasInvincible: hasUtilityCategory(job, '무적'),
        hasRevive: hasUtilityCategory(job, '사망 1회 방지') || hasUtilityCategory(job, '부활'),
        hasPartySupport: JOB_UTILITY_COMPLETE.filter(u => u.job === job && u.category === '기타').some(u => u.effect.includes('파티')),
        hasTeleport: hasUtilityCategory(job, '텔레포트')
    }
}
